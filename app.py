values = [
area["월세_score"],
area["문화_score"],
area["공원_score"],
area["물가_score"]
]

fig = go.Figure()

fig.add_trace(go.Scatterpolar(
r=values,
theta=["월세","문화","공원","물가"],
fill='toself',
name=select_area
))

fig.update_layout(
polar=dict(
radialaxis=dict(visible=True)
),
showlegend=False
)

st.plotly_chart(fig,use_container_width=True)

